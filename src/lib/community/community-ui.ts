export function communityRankTone(rank: number): { bg: string; text: string; border: string } {
  if (rank === 1) return { bg: "rgba(52,211,153,0.12)", text: "#6ee7b7", border: "rgba(52,211,153,0.35)" };
  if (rank === 2) return { bg: "rgba(34,211,238,0.1)", text: "#67e8f9", border: "rgba(34,211,238,0.3)" };
  if (rank === 3) return { bg: "rgba(251,191,36,0.1)", text: "#fcd34d", border: "rgba(251,191,36,0.3)" };
  return { bg: "rgba(255,255,255,0.04)", text: "#a8a29e", border: "rgba(255,255,255,0.1)" };
}
